/*

................Dart Operators.................


Operators are special symbols that perform specific operations on one, two, or more operands and then return a result.



1. Arithmetic Operators : +, - , *, /, %, ++, --

2. Ternary Operator : (condition ? expr1 : expr2)

for example : 

  int a = 10;
  int b = 20;
  var min = (a < b) ? a : b; // if a is less than b then assign a to min otherwise assign b to min
  print(min); // output: 10

  3. Logical Operators : && , || , !

  for example: 
         if (a > b && b < c) {
          print("Both conditions are true");
          }

 4.  OR operator : |

  for example: 
         if (a > b | b < c) {
          print("At least one condition is true");
          }






 */