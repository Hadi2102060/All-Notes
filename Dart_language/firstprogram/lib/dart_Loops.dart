/*

..........................Dart LOOPs.....................

// A loop is used for dart to execute a block of code repeatedly until a certain condition is met.

There are three types of loops in Dart:
1. for loop
2. while loop 
3. do-while loop

For example:

      // for loop
      for (int i = 1; i <= 5; i++) {
        print("For Loop: $i");
      }

      // while loop
      int j = 1;
      while (j <= 5) {
        print("While Loop: $j");
        j++;
      }

      // do-while loop
      int k = 1;
      do {
        print("Do-While Loop: $k");
        k++;
      } while (k <= 5);












 */